package bab03.code02;

public class Inc01 {
    public static void main(String[] args) {
        // Buat variabel awal
        int a = 0;
        int b = 0;
        int c = 9;
        int d = 9;
        // Print ke 1
        System.out.println("Print ke 1");
        System.out.println("A = " + a++);
        System.out.println("B = " + ++b);
        System.out.println("C = " + c--);
        System.out.println("D = " + --d);
    }
}
