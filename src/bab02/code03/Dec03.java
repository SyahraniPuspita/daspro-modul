public class Dec03 {
    public static void main(String[] args) {
        double pi = 3.14;
        int jarijari = 7;

        double luasDouble = pi * jarijari * jarijari;
        int luasInt = (int) (pi * jarijari * jarijari);

        System.out.println("Luas Lingkaran");
        System.out.println("Double = " + luasDouble);
        System.out.println("Int = " + luasInt);
    }
}
